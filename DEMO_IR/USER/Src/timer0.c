#include "timer0.h"

/**************************************************
*函数名称：TIMER0_Init	
*函数功能：T0初始化
*入口参数：无	        	
*出口参数：无
*功能说明：定时40us（640/16=40）
**************************************************/
void TIMER0_Init(void)
{
	TMOD |= 0X01;
	TMCON |= 0X01;
	TL0 = (65536 - 640)%256;  
	TH0 = (65536 - 640)/256;
	ET0 = 1;
	TR0 = 1;	
}

