#include "gpio.h"

/**************************************************
*函数名称：GPIO_Init	
*函数功能：GPIO初始化
*入口参数：无	        	
*出口参数：无
*功能说明：
**************************************************/
void GPIO_Init(void)
{	
	P1CON |= 0X0E;		 //TM1638通讯线强推挽
	P2CON &= ~0X02;		 
	
	//P2CON |= 0X01;		 //TM1638片选线强推挽
	P2CON |= 0Xf1;		 //TM1638片选线强推挽
	P2CON &= ~0X04;		 //红外接收口高阻输入
	P2PH |= 0X04;	
}